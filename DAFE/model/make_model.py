class net1(nn.Module):
    def __init__(self, in_channels, ratio=1):
        super(net1, self).__init__()
        self.avg_pool = nn.AdaptiveAvgPool2d(1)
        self.fc = nn.Sequential(

            nn.Linear(in_channels, in_channels // ratio, False),  # 不使用偏置量，设为False
            nn.ReLU(),
            nn.Linear(in_channels // ratio, in_channels, False),
            nn.Sigmoid(),
        )

    def forward(self, x):
        b, c, h, w = x.size()
        avg = self.avg_pool(x).view([b, c])
        fc = self.fc(avg).view([b, c, 1, 1])
        return x * fc
class net2(nn.Module):
    def __init__(self, in_channels, reduction_ratio=1):
        super(net2, self).__init__()
        self.avg_pool = nn.AdaptiveAvgPool2d(1)
        self.fc = nn.Sequential(
            nn.Linear(in_channels, in_channels // reduction_ratio),
            nn.ReLU(inplace=True),
            nn.Linear(in_channels // reduction_ratio, in_channels),
            nn.Sigmoid()
        )

    def forward(self, x):
        batch_size, num_channels, H, W = x.size()
        x_pool = self.avg_pool(x)
        x_pool = x_pool.view(batch_size, num_channels)
        x_fc = self.fc(x_pool)
        x_fc = x_fc.view(batch_size, num_channels, 1, 1)
        x_fc = x_fc.expand_as(x)
        x = x * x_fc
        x = x.sum(dim=1, keepdim=True)

        return x
class RFE(nn.Module):
    def __init__(self, in_channels, reduction_ratio=1):
        super(RFE, self).__init__()
        self.N1 = net1(in_channels, reduction_ratio)
        self.N2 = net2(in_channels, reduction_ratio)

    def forward(self, x):
        N1_out = self.N1(x)
        N2_out = self.N2(x)
        out = N1_out + N2_out
        return out

class DLocal(nn.Module):
    def __init__(self, in_dim):
        super(DLocal, self).__init__()
        self.conv_query = nn.Linear(in_dim, in_dim)
        self.conv_part = nn.Linear(in_dim, in_dim)
        self.conv_value = nn.Linear(in_dim, in_dim)
        self.softmax = nn.Softmax(dim=-1)
        self.param = nn.Parameter(torch.zeros(1))

    def forward(self, query, part):
        f_query = self.conv_query(query).unsqueeze(1) # print(f_query.shape)[16,1,768]
        f_part = self.conv_part(part).unsqueeze(2) # print(f_part.shape)[16,768,1]
        f_value = self.conv_value(part).unsqueeze(2)
        energy = torch.matmul(f_query, f_part)
        similarity = self.softmax(energy)  # print(similarity.shape)[16,1,1]
        f_value = torch.matmul(f_part, similarity) + f_part   # print(f_value.shape)[16,768,1]
        # print(self.param)
        final_feat = query.unsqueeze(2) + torch.matmul(f_value, self.param.unsqueeze(0))
        # final_feat = query.unsqueeze(2) + f_value
        # print(final_feat.shape)[16,768,1]
        return final_feat.squeeze(2)

