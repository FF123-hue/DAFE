    def forward(self, x, x2, x3, label=None, cam_label= None, view_label=None):  # label is unused if self.cos_layer == 'no'

        features = self.base(x, cam_label=cam_label, view_label=view_label)
        features2 = self.base(x2, cam_label=cam_label, view_label=view_label)
        features3 = self.base(x3, cam_label=cam_label, view_label=view_label)
        res1 = self.res(x)*0.01
        res2 = self.res(x2)*0.01
        res3 = self.res(x3)*0.01
        batch_size = features.shape[0]
        channels = 1  # 假设是灰度图像
        height = features.shape[1]
        width = features.shape[2]
        features_re = features.reshape(batch_size, channels, height, -1)
        batch_size = features2.shape[0]
        channels = 1
        height = features2.shape[1]
        features2_re = features2.reshape(batch_size, channels, height, -1)
        batch_size = features3.shape[0]
        height = features3.shape[1]
        features3_re = features3.reshape(batch_size, channels, height, -1)

        features_sp = self.rfe(features_re)
        features2_sp = self.rfe(features2_re)
        features3_sp = self.rfe(features3_re)

        features_sp = features_sp.squeeze(1)
        features2_sp = features2_sp.squeeze(1)
        features3_sp = features3_sp.squeeze(1)


        features = features_sp + features
        features2 = features2_sp + features2
        features3 = features3_sp + features3
        # global branch
        b1_feat = self.b1(features) # [64, 129, 768] 批大小、长度、维度
        global_feat_ori = b1_feat[:, 0]
        global_feat_ori = global_feat_ori+res1
        # print("11111111", global_feat_ori.shape)
        # global branch
        b1_feat_occ1 = self.b1(features2) # [64, 129, 768]
        global_feat2 = b1_feat_occ1[:, 0]
        global_feat2 = global_feat2+res2
        # global branch
        b1_feat_occ2 = self.b1(features3) # [64, 129, 768]
        global_feat3 = b1_feat_occ2[:, 0]
        global_feat3 = global_feat3+res3
        # JPM branch
        feature_length = features.size(1) - 1
        patch_length = feature_length // self.divide_length
        token = features[:, 0:1]

        if self.rearrange:
            x = shuffle_unit(features, self.shift_num, self.shuffle_groups)
        else:
            x = features[:, 1:]
        # lf_1
        b1_local_feat = x[:, :patch_length]
        b1_local_feat = self.b2(torch.cat((token, b1_local_feat), dim=1))
        local_feat_1 = b1_local_feat[:, 0]

        # lf_2
        b2_local_feat = x[:, patch_length:patch_length*2]
        b2_local_feat = self.b2(torch.cat((token, b2_local_feat), dim=1))
        local_feat_2 = b2_local_feat[:, 0]

        # lf_3
        b3_local_feat = x[:, patch_length*2:patch_length*3]
        b3_local_feat = self.b2(torch.cat((token, b3_local_feat), dim=1))
        local_feat_3 = b3_local_feat[:, 0]

        # lf_4
        b4_local_feat = x[:, patch_length*3:patch_length*4]
        b4_local_feat = self.b2(torch.cat((token, b4_local_feat), dim=1))
        local_feat_4 = b4_local_feat[:, 0]

        local_feat_1 = self.DAL[0](local_feat_1, global_feat_ori) + local_feat_1
        local_feat_2 = self.DAL[1](local_feat_2, global_feat_ori) + local_feat_2
        local_feat_3 = self.DAL[2](local_feat_3, global_feat_ori) + local_feat_3
        local_feat_4 = self.DAL[3](local_feat_4, global_feat_ori) + local_feat_4

        global_feat = self.DAL[0](global_feat_ori, local_feat_1)
        global_feat = self.DAL[1](global_feat, local_feat_2)
        global_feat = self.DAL[2](global_feat, local_feat_3)
        global_feat = self.DAL[3](global_feat, local_feat_4)


        # feat = self.bottleneck(global_feat)
        feat = self.bottleneck(global_feat)
        feat2 = self.bottleneck_occ1(global_feat2)
        feat3 = self.bottleneck_occ2(global_feat3)

        local_feat_1_bn = self.bottleneck_1(local_feat_1)
        local_feat_2_bn = self.bottleneck_2(local_feat_2)
        local_feat_3_bn = self.bottleneck_3(local_feat_3)
        local_feat_4_bn = self.bottleneck_4(local_feat_4)

        if self.training:
            if self.ID_LOSS_TYPE in ('arcface', 'cosface', 'amsoftmax', 'circle'):
                cls_score = self.classifier(feat, label)
            else:
                cls_score = self.classifier(feat)
                cls_score_occ1 = self.classifier_occ1(feat2)
                cls_score_occ2 = self.classifier_occ2(feat3)
                cls_score_1 = self.classifier_1(local_feat_1_bn)
                cls_score_2 = self.classifier_2(local_feat_2_bn)
                cls_score_3 = self.classifier_3(local_feat_3_bn)
                cls_score_4 = self.classifier_4(local_feat_4_bn)
            return [cls_score, cls_score_occ1, cls_score_occ2, cls_score_1, cls_score_2, cls_score_3,
                        cls_score_4
                        ], [global_feat, global_feat2, global_feat3, local_feat_1, local_feat_2, local_feat_3,
                            local_feat_4]  # global feature for triplet loss
        else:
            if self.neck_feat == 'after':
                return torch.cat(
                    [feat, local_feat_1_bn / 4, local_feat_2_bn / 4, local_feat_3_bn / 4, local_feat_4_bn / 4], dim=1)
            else:
                return torch.cat(
                    [global_feat, local_feat_1, local_feat_2, local_feat_3, local_feat_4], dim=1)

    def load_param(self, trained_path):
        param_dict = torch.load(trained_path)
        for i in param_dict:
            self.state_dict()[i.replace('module.', '')].copy_(param_dict[i])
        print('Loading pretrained model from {}'.format(trained_path))

    def load_param_finetune(self, model_path):
        param_dict = torch.load(model_path)
        for i in param_dict:
            self.state_dict()[i].copy_(param_dict[i])
        print('Loading pretrained model for finetuning from {}'.format(model_path))


